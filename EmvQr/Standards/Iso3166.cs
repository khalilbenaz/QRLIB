        /// <summary>
        /// Checks if the provided country code is a valid ISO 3166-1 alpha-2 code
        /// </summary>
        /// <param name="countryCode">The 2-letter country code to validate</param>
        /// <returns>True if valid, false otherwise</returns>
        public static bool IsValid(string countryCode)
        {
            if (string.IsNullOrEmpty(countryCode) || countryCode.Length != 2)
                return false;

            return ValidCountries.Contains(countryCode);
        }
